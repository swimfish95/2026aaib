// week11-5.cpp SOIT108_Advance_002
#include <stdio.h>

int main()
{
    int a, b;
    // 讀取兩個數字 a 和 b，代表我們要尋找的範圍 (例如從 10 找 到 20)
    scanf("%d%d", &a, &b);

    int ans = 0; // 用來記錄「總共找到了幾個質數」

    // 外層迴圈 i：從 a 一路檢查到 b，每個數字都拿來測試看看
    for (int i=a; i<=b; i++) {

        int bad = 0; // 🚩 設立一個「壞寶寶旗標」，0 代表預設它是質數 (好寶寶)

        // 內層迴圈 k：用來測試 i 是不是質數
        // 質數的定義是「只能被 1 和自己整除」。所以我們從 2 開始除，一路除到 i-1
        for (int k=2; k<i; k++) {
            // 藍色反白區塊：如果 i 竟然可以被 k 整除 (餘數為 0)
            if (i % k == 0) {
                bad = 1; // 抓到了！它有其他的因數，把它標記為壞寶寶 (bad = 1)
            }
        }

        // 如果經歷了上面的層層考驗，bad 依然是 0，代表它真的是質數！
        if (bad == 0) {
            ans++; // 質數數量 + 1
        }
    }

    // 印出最終找到了幾個質數
    printf("%d\n", ans);

    return 0;
}
